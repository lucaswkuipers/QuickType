import CoreGraphics

extension Double {
    var half: Double {
        return self / 2
    }
}

extension Float {
    var half: Float {
        return self / 2
    }
}

extension CGFloat {
    var half: CGFloat {
        return self / 2
    }
}

extension Int {
    var half: Int {
        return self / 2
    }
}
